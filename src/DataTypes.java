
public class DataTypes {
	public static void main(String[] args) {
		byte bytex; // whole number between -128 and 127
		bytex = 123;
		System.out.println(bytex);
		
		short shortx; //-32,768 to 32767
		shortx = 2345;
		System.out.println(shortx);
		
		long longx = -12332252626L; // Used when int is not large enough to hold the value
    	System.out.println(longx);
    	
    	double doublex = -42937737.9d; //Sufficient for holding 15 decimal digits
    	System.out.println(doublex);
    	
    	
		
	}
}
